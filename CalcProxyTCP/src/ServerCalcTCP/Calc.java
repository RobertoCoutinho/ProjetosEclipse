package ServerCalcTCP;

public class Calc {
	public double add(double n1, double n2){
		double res;
		res = n1 + n2;
		return res;
	}
	
	public double sub(double n1, double n2){
		double res;
		res = n1 - n2;
		return res;
	}
	
	public double mult(double n1, double n2){
		double res;
		res = n1 * n2;
		return res;
	}
	
	public double div(double n1, double n2){
		double res;
		res = n1 / n2;
		return res;
	}
}
